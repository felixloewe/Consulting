## Übersicht externer Kovariablen ERGM-Modelle

## Kantenattribute: Attribute beider Länder

# Alliance Kantenattribut: Allianzen ja/nein
# Polity Kantenattribut: Unterschied im Demokratie-Score, Skala 0 bis 20
# Neighbour: Gibt an, ob die Länder benachbart sind ja/nein
# Path Dependency: Summiert den Waffenwert für die letzten 4 Jahre

## Knotenattribute: Attribute eines Landes

# GDP Knotenattribut: Bruttosozialprodukt, Skala Internationaler Dollar
# Conflict Knotenattribut : Konfliktsintensität, eine Zeile ein innerer Konflikt
# CINC Knotenattribut: Composite Index of National Capability (Statistical Measure of National Power) 
# (von Correlates of War-Projekt)
# Polity Knotenattribut: Demokratie-Score, Skala -10 bis 10


